def get_dsn() -> str:
    if "DATABASE_URL" not in st.secrets:
        raise ValueError("Missing DATABASE_URL in Streamlit Secrets (Manage app → Settings → Secrets).")
    return str(st.secrets["DATABASE_URL"]).strip()


@contextmanager
def get_conn():
    # Open a fresh connection per operation (safe + reliable on Streamlit Cloud)
    conn = psycopg.connect(get_dsn(), autocommit=False)
    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


def query_df(sql: str, params: Optional[Tuple[Any, ...]] = None) -> pd.DataFrame:
    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute(sql, params or ())
            if cur.description is None:
                return pd.DataFrame()
            cols = [c.name for c in cur.description]
            rows = cur.fetchall()
            return pd.DataFrame(rows, columns=cols)


def exec_sql(sql: str, params: Optional[Tuple[Any, ...]] = None) -> None:
    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute(sql, params or ())


def fetch_one(sql: str, params: Optional[Tuple[Any, ...]] = None) -> Optional[Tuple[Any, ...]]:
    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute(sql, params or ())
            return cur.fetchone()


