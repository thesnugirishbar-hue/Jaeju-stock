def get_menu_items(active_only=True) -> pd.DataFrame:
    sql = "SELECT id, sku, name, price, active, sort_order FROM menu_items"
    if active_only:
        sql += " WHERE active = TRUE"
    sql += " ORDER BY sort_order ASC, name ASC"
    return query_df(sql)


def upsert_menu_items(df: pd.DataFrame):
    with get_conn() as conn:
        with conn.cursor() as cur:
            for _, r in df.iterrows():
                sku = str(r.get("sku", "")).strip()
                name = str(r.get("name", "")).strip()
                if not sku or not name:
                    continue
                price = float(r.get("price", 0.0))
                active = bool(r.get("active", True))
                sort_order = int(r.get("sort_order", 0))

                rid = r.get("id", None)
                if pd.isna(rid):
                    cur.execute(
                        """
                        INSERT INTO menu_items(sku, name, price, active, sort_order)
                        VALUES (%s, %s, %s, %s, %s)
                        ON CONFLICT (sku) DO UPDATE
                          SET name=EXCLUDED.name, price=EXCLUDED.price, active=EXCLUDED.active, sort_order=EXCLUDED.sort_order
                        """,
                        (sku, name, price, active, sort_order),
                    )
                else:
                    cur.execute(
                        """
                        UPDATE menu_items
                        SET sku=%s, name=%s, price=%s, active=%s, sort_order=%s
                        WHERE id=%s
                        """,
                        (sku, name, price, active, sort_order, int(rid)),
                    )


def get_menu_recipe(menu_id: int) -> pd.DataFrame:
    return query_df(
        """
        SELECT mr.id, mr.menu_id, i.id AS item_id, i.name AS item_name, mr.qty
        FROM menu_recipes mr
        JOIN items i ON i.id = mr.item_id
        WHERE mr.menu_id = %s AND i.active = TRUE
        ORDER BY i.name ASC
        """,
        (int(menu_id),),
    )


def upsert_menu_recipe(menu_id: int, df: pd.DataFrame):
    # simplest: delete + insert
    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute("DELETE FROM menu_recipes WHERE menu_id = %s", (int(menu_id),))
            for _, r in df.iterrows():
                try:
                    item_id = int(r["item_id"])
                    qty = float(r["qty"])
                except Exception:
                    continue
                if item_id <= 0 or qty == 0:
                    continue
                cur.execute(
                    """
                    INSERT INTO menu_recipes(menu_id, item_id, qty)
                    VALUES (%s, %s, %s)
                    ON CONFLICT (menu_id, item_id) DO UPDATE SET qty=EXCLUDED.qty
                    """,
                    (int(menu_id), int(item_id), float(qty)),
                )


def get_recipe_map(menu_id: int) -> Dict[int, float]:
    df = query_df("SELECT item_id, qty FROM menu_recipes WHERE menu_id = %s", (int(menu_id),))
    if df.empty:
        return {}
    return {int(r["item_id"]): float(r["qty"]) for _, r in df.iterrows()}


