def ensure_stock_rows_for_item(item_id: int):
    with get_conn() as conn:
        with conn.cursor() as cur:
            for loc in LOCATIONS:
                cur.execute(
                    """
                    INSERT INTO stock(item_id, location, qty)
                    VALUES (%s, %s, 0)
                    ON CONFLICT (item_id, location) DO NOTHING
                    """,
                    (item_id, loc),
                )


def get_items_df(active_only=True) -> pd.DataFrame:
    sql = "SELECT id, name, unit, par_level, price_nzd, active FROM items"
    if active_only:
        sql += " WHERE active = TRUE"
    sql += " ORDER BY name ASC"
    return query_df(sql)


def add_item(name: str, unit: str, par_level: float, price_nzd: float, active: bool = True):
    name = (name or "").strip()
    if not name:
        raise ValueError("Item name cannot be empty.")

    unit = (unit or "unit").strip() or "unit"

    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute(
                """
                INSERT INTO items(name, unit, par_level, price_nzd, active)
                VALUES (%s, %s, %s, %s, %s)
                ON CONFLICT (name) DO UPDATE
                  SET unit = EXCLUDED.unit,
                      par_level = EXCLUDED.par_level,
                      price_nzd = EXCLUDED.price_nzd,
                      active = EXCLUDED.active
                RETURNING id
                """,
                (name, unit, float(par_level), float(price_nzd), bool(active)),
            )
            item_id = int(cur.fetchone()[0])

    ensure_stock_rows_for_item(item_id)


def get_item_id_by_name(name: str) -> Optional[int]:
    row = fetch_one("SELECT id FROM items WHERE name = %s AND active = TRUE", (name,))
    return int(row[0]) if row else None


def adjust_stock(item_id: int, location: str, delta: float, reason: str, ref_type: str = "", ref_id: Optional[int] = None):
    if location not in LOCATIONS:
        raise ValueError("Invalid location.")
    if not (reason or "").strip():
        raise ValueError("Reason is required.")

    ensure_stock_rows_for_item(int(item_id))

    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute(
                """
                UPDATE stock
                SET qty = qty + %s
                WHERE item_id = %s AND location = %s
                """,
                (float(delta), int(item_id), location),
            )
            cur.execute(
                """
                INSERT INTO movements(created_at, item_id, location, delta, reason, ref_type, ref_id)
                VALUES (%s, %s, %s, %s, %s, %s, %s)
                """,
                (now_iso(), int(item_id), location, float(delta), reason, ref_type or None, ref_id),
            )


def get_stock_df() -> pd.DataFrame:
    return query_df(
        """
        SELECT
          s.item_id AS item_id,
          i.name AS name,
          i.unit AS unit,
          i.par_level AS par_level,
          s.location AS location,
          s.qty AS qty
        FROM stock s
        JOIN items i ON i.id = s.item_id
        WHERE i.active = TRUE
        ORDER BY i.name ASC, s.location ASC
        """
    )


def get_stock_pivot() -> pd.DataFrame:
    df = get_stock_df()
    if df.empty:
        return df
    pivot = (
        df.pivot_table(
            index=["item_id", "name", "unit", "par_level"],
            columns="location",
            values="qty",
            aggfunc="sum",
        )
        .reset_index()
    )
    for loc in LOCATIONS:
        if loc not in pivot.columns:
            pivot[loc] = 0.0
    pivot["Below PAR?"] = (pivot[LOC_PREP] < pivot["par_level"]).map({True: "YES", False: ""})
    return pivot


