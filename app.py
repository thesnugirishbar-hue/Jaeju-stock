def init_db():
    # Tables are created once and never wiped.
    exec_sql(
        """
        CREATE TABLE IF NOT EXISTS items (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name TEXT NOT NULL UNIQUE,
            unit TEXT NOT NULL,
            par_level DOUBLE PRECISION NOT NULL DEFAULT 0,
            price_nzd DOUBLE PRECISION NOT NULL DEFAULT 0,
            active BOOLEAN NOT NULL DEFAULT TRUE
        );

        CREATE TABLE IF NOT EXISTS stock (
            item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
            location TEXT NOT NULL,
            qty DOUBLE PRECISION NOT NULL DEFAULT 0,
            PRIMARY KEY (item_id, location)
        );

        CREATE TABLE IF NOT EXISTS movements (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TEXT NOT NULL,
            item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
            location TEXT NOT NULL,
            delta DOUBLE PRECISION NOT NULL,
            reason TEXT NOT NULL,
            ref_type TEXT,
            ref_id BIGINT
        );

        CREATE TABLE IF NOT EXISTS orders (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TEXT NOT NULL,
            from_location TEXT NOT NULL,
            to_location TEXT NOT NULL,
            status TEXT NOT NULL,
            note TEXT
        );

        CREATE TABLE IF NOT EXISTS order_lines (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            order_id BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
            item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
            qty DOUBLE PRECISION NOT NULL
        );

        CREATE TABLE IF NOT EXISTS sales (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TEXT NOT NULL,
            sale_date TEXT NOT NULL,
            payment_method TEXT NOT NULL,
            note TEXT
        );

        CREATE TABLE IF NOT EXISTS sale_lines (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            sale_id BIGINT NOT NULL REFERENCES sales(id) ON DELETE CASCADE,
            menu_id BIGINT NOT NULL,
            sku TEXT NOT NULL,
            name TEXT NOT NULL,
            qty DOUBLE PRECISION NOT NULL,
            unit_price DOUBLE PRECISION NOT NULL,
            line_total DOUBLE PRECISION NOT NULL
        );

        CREATE TABLE IF NOT EXISTS menu_items (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            sku TEXT NOT NULL UNIQUE,
            name TEXT NOT NULL,
            price DOUBLE PRECISION NOT NULL DEFAULT 0,
            active BOOLEAN NOT NULL DEFAULT TRUE,
            sort_order BIGINT NOT NULL DEFAULT 0
        );

        CREATE TABLE IF NOT EXISTS menu_recipes (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            menu_id BIGINT NOT NULL REFERENCES menu_items(id) ON DELETE CASCADE,
            item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
            qty DOUBLE PRECISION NOT NULL,
            UNIQUE(menu_id, item_id)
        );

        CREATE INDEX IF NOT EXISTS idx_movements_created_at ON movements(created_at);
        CREATE INDEX IF NOT EXISTS idx_orders_created_at ON orders(created_at);
        CREATE INDEX IF NOT EXISTS idx_sales_sale_date ON sales(sale_date);
        """
    )


